/**
 * Data associated to every user
 */
export interface User {
  /**
   * First name of the user
   */
  firstName: string;
  /**
   * Surname of the user
   */
  surname: string;
  /**
   * Date of birth of the user
   */
  dateOfBirth: Date;
  /**
   * Email of the user
   */
  email: string;
}
